//Return the sum of two numbers
function addition(a, b) {
  return a + b;
}
addition(2, 3);

// Return the remainder of two numbers
function remainder(x, y) {
  return x % y;
}
remainder(3, 1);

// Swap two given numbers

function swap(a, b) {
  return [b, a];
}
swap(10, 26);

// Return the fist element in an array
function getFirstValue(arr) {
  return arr[0];
}
getFirstValue([1, 2, 5, 9]);

